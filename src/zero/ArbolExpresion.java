        // TODO add your handling code here:
        String expre = Expresion.getText();
        ArbolExpresiones expr = new ArbolExpresiones();
        NodoArbol raiz = expr.construirArbol(expre);

        String inorden = expr.imprime(raiz).trim();
        String postorden = expr.imprimePos(raiz).trim();
        String preorden = expr.imprimePre(raiz).trim();

        String resultado = "El arbol es: " + inorden + "\nEl arbol en postfija es: " + postorden + "\nEl arbol en prefija es: " + preorden;

        Resultado.setText(resultado);
